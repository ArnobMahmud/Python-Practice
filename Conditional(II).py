num = float(input("Enter your number :"))

if num >= 80.00:
    print("Your grade is A+.")
elif num >= 70.00:
    print("Your grade is A.")
elif num >= 60.00:
    print("Your grade is A-.")
elif num >= 50.00:
    print("Your grade is B.")
elif num >= 40.00:
    print("Your grade is C.")
elif num >= 33.00:
    print("Your grade is D.")
else:
    print("You are fail")